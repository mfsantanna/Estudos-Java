public class OperadorIncremento
{
    public static void main(String[] args)
    {
        int numero = 5;

        numero ++; //incrementa o valor da variável numero em um e salva na variável numero

        System.out.println(numero);
        
        System.out.println(--numero); // Para usar o inc. ou dec. direto na impressão, preciso sinalizar antes da variável

        boolean variavel = true;

        System.out.println(!variavel); //inverte o estado da variavel
    }
}